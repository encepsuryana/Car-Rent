        private void ubah_mobilActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ubah_mobilActionPerformed
        int  ubah= JOptionPane.showConfirmDialog(null,"Apakah anda ingin Mengubah data ini?", "Konfirmasi",JOptionPane.YES_NO_OPTION);
        
        if(ubah==JOptionPane.YES_OPTION) {
                
            String NoPol  = no_polisi.getText();
            String WarMob  = warna_mobil.getText();
            String MerMob = merk_mobil.getSelectedItem().toString();
            String TyeMob = type_mobil.getText();
            String HargMo = harga_mobil.getText();
          
        if ((WarMob.isEmpty()) || (MerMob.isEmpty())){
            JOptionPane.showMessageDialog(null,"Data Tidak Boleh Kosong, Slilahkan Dilengkapi");
            warna_mobil.requestFocus();
        } else {
            try {
                    Class.forName(driver);
                    Connection kon = (Connection) DriverManager.getConnection(database,user, pass);
                    Statement stt = (Statement) kon.createStatement();
                    String SQL = "UPDATE `tabel_mobil` "
                                    + "SET `No_Polisi`= '"+NoPol+"',"
                                    + "`Warna_Mobil`= '"+WarMob+"',"
                                    + "`Merk_Mobil`= '"+MerMob+"',"
                                    + "`Type_Mobil`= '"+TyeMob+"',"
                                    + "`Harga`= '"+HargMo+"'"
                            + "WHERE "
                            + "`No_Polisi`='"+tableModel.getValueAt(row, 0).toString()+"';";
                    stt.executeUpdate(SQL);
                    data[0] = NoPol;
                    data[1] = WarMob;
                    data[2] = MerMob;
                    data[3] = TyeMob;
                    data[4] = HargMo;
                    
                    tableModel.removeRow(row);
                    tableModel.insertRow(row, data);
                    stt.close();
                    kon.close();
                    tbl_mobil.clearSelection();

                    tableModel.setRowCount(0);
                    settableload();
                                        
                    membersihkan_teks();
                    simpan_mobil.setEnabled(true);
                    hapus_mobil.setEnabled(true);
                } catch (Exception ex) {
                 System.err.println(ex.getMessage());
            }
         }
            } else if(ubah==JOptionPane.NO_OPTION) {
            batal_mobilActionPerformed(evt);
        }
    }//GEN-LAST:event_ubah_mobilActionPerformed

