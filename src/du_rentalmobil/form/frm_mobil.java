    private void simpan_mobilActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_simpan_mobilActionPerformed
        String data[]=new String[5];

        if (warna_mobil.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Data Tidak Boleh Kosong, Silahkan dilengkapi");
            warna_mobil.requestFocus();
        } else {
        try {
         Class.forName(driver);
         Connection kon = (Connection) DriverManager.getConnection(database, user, pass);
         Statement stt = (Statement) kon.createStatement();
         String SQL = "INSERT INTO tabel_mobil (No_Polisi,"
                                                       + "Warna_Mobil,"
                                                       + "Merk_Mobil,"
                                                       + "Type_mobil,"
                                                       + "Harga)"
                                                       + "VALUES" 
                                                       + "('"+no_polisi.getText()+"',"                
                                                       + "'"+warna_mobil.getText()+"',"
                                                       + "'"+merk_mobil.getSelectedItem().toString()+"',"
                                                       + "'"+type_mobil.getText()+"',"
                                                       + "'"+harga_mobil.getText()+"')";
                      stt.executeUpdate(SQL);
                      data[0]  = no_polisi.getText();                     
                      data[1]  = warna_mobil.getText();
                      data[2]  = merk_mobil.getSelectedItem().toString();
                      data[3]  = type_mobil.getText();
                      data[4]  = harga_mobil.getText();

                      tableModel.setRowCount(0);
                      settableload();
                      stt.close();
                      kon.close();
                      membersihkan_teks();
                      simpan_mobil.setEnabled(true);
                      ubah_mobil.setEnabled(true);
                      hapus_mobil.setEnabled(true);
                      batal_mobil.setEnabled(true);
                      
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(null, ex.getMessage(),"Gagal Terhubung Ke Database", JOptionPane.INFORMATION_MESSAGE);
            }
        }
    }

