    private void hapus_mobilActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_hapus_mobilActionPerformed
        int  hapus= JOptionPane.showConfirmDialog(null,"Apakah anda ingin Menghapus data ini?", "Konfirmasi",JOptionPane.YES_NO_OPTION);
        
        if(hapus==JOptionPane.YES_OPTION) {
        int baris = tbl_mobil.getSelectedRow();
            if (baris != -1) {
                String nopolisi = tbl_mobil.getValueAt(baris, 0).toString();
                String SQL = "DELETE FROM tabel_mobil WHERE No_Polisi='"+nopolisi+"'";
                tableModel.removeRow(row);
                membersihkan_teks();
                int status = du_rentalmobil.koneksi.koneksiRental.execute(SQL);
                if (status==1) {
                    JOptionPane.showMessageDialog(this, "Data berhasil dihapus", "Sukses", JOptionPane.INFORMATION_MESSAGE);
                } else {
                    JOptionPane.showMessageDialog(this, "Data gagal dihapus", "Gagal", JOptionPane.WARNING_MESSAGE);
                }
            } else {
                JOptionPane.showMessageDialog(this, "Pilih Baris Data Terlebih dahulu", "Error !!", JOptionPane.WARNING_MESSAGE);
            }
        } else if(hapus==JOptionPane.NO_OPTION) {
            batal_mobilActionPerformed(evt);        }
            aktif_teks();
    }//GEN-LAST:event_hapus_mobilActionPerformed

